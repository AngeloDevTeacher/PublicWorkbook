// There are mutliple number type of variables:
// int, float, double, decimal, and more!
// We will use DOUBLE for most of the class.


// ints are whole numbers
int thisIsAnInt = 12;

// Floats have a "f" at the end of the number.
float thisIsAFloat = 12.4f;

// Doubles do NOT have a unit at the end of a number.
double thisIsADouble = 12.4;

// Decimals have a "m" at the end of the number
decimal thisIsADecimal = 12.4m;


// Char and string are "character" and "word" types:

// Char uses ' '
char thisIsAChar = 'a';
// string uses " "
string thisIsAString = "A";

// Booleans are true/false values:
bool thisIsABoolean = true;
bool thisIsAFalseBoolean = false;

// Finally there is the null value! It can exist on certain types.
string thisIsANulledString = null;

